# Types of data in python are
# 1. Numeric
#    a)Integer
#    b)Float/Double
#    c)Complex Number
# 2. Sequence
#    a) String
#    b) Lists
#    c) Tuples
# 3. Dictionary
# 1. Boolean
# 1. Set

# Numeric
# a) Integer
age = 25

# b) Float/Double
height = 5.9

# c) Complex Number
z = 3 + 4j

# Sequence
# a) String
name = "John"

# b) List
numbers = [1, 2, 'a', 4]

# c) Tuple
coordinates = (3, 5)

# Dictionary
student_info = {
    'name': 'Alice',
    'age': 20,
    'grade': 'A'
}

# Boolean
is_adult = True

# Set
unique_numbers = {1, 'a', 3, 4, 5}

# Printing the values
print("Integer:", age)
print("Float:", height)
print("Complex Number:", z)
print("String:", name)
print("List:", numbers)
print("Tuple:", coordinates)
print("Dictionary:", student_info)
print("Boolean:", is_adult)
print("Set:", unique_numbers)
print(type(student_info))
